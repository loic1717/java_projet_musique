    private void bt_enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_enregistrerActionPerformed
        // TODO add your handling code here:

        String motdepasse,ancienmotpasse,nouveaumotpasse;

        ancienmotpasse=ed_mdpc.getText();
        nouveaumotpasse=edmdpnew.getText();

        if(edmdpnew.getText().equals(ed_mdpconfir.getText())){

            
            try {
                leControl.modif_MP_Utilisateurs(ancienmotpasse, nouveaumotpasse);
                
                //
            } catch (SQLException ex) {
                Logger.getLogger(V_MonCompte.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } else{
            JOptionPane.showMessageDialog(null, "Mot de passe actuel incorrect.");
        }

    }//GEN-LAST:event_bt_enregistrerActionPerformed

