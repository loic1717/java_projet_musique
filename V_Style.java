    public void afficher(LinkedHashMap<Integer, M_Style> lesStyles){
    this.lesStyles = lesStyles;
    dm_tb_Styles = (DefaultTableModel) tb_Style.getModel();
        
        ligne = 0;
        nbStyles = lesStyles.size();
        dm_tb_Styles.setRowCount(nbStyles);
        
        
        for (Integer uneCle : lesStyles.keySet()){
            unStyle = this.lesStyles.get(uneCle);
            tb_Style.setValueAt(unStyle.getIdStyle(), ligne, 0);
            tb_Style.setValueAt(unStyle.getLibelle(), ligne, 1);
            ligne++;
        }
       tb_Style.clearSelection();
        
        this.setSize(450, 350);
        this.setLocationRelativeTo(null);
        pn_CMS_Styles.setBounds(15, 15, 400, 300);
        pn_Saisie_Styles.setBounds(15, 15, 400, 400);
        pn_CMS_Styles.setVisible(true);
        pn_Saisie_Styles.setVisible(false);

    this.setVisible(true);
    }
    
