    private void bt_EnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_EnregistrerActionPerformed
        // TODO add your handling code here:
         switch(mode){
            case 'M':   ligneSelect = tb_Style.getSelectedRow();
                        uneCle = (Integer) tb_Style.getValueAt(ligneSelect,0);
             {
                 try {
                     leControl.modif_Style(uneCle,ed_Libelle.getText());
                 } catch (SQLException ex) {
                     Logger.getLogger(V_Style.class.getName()).log(Level.SEVERE, null, ex);
                 }
             }
                        break;

            case 'A':   
             {
                 try {
                     leControl.ajout_Style(ed_Libelle.getText());
                 } catch (SQLException ex) {
                     Logger.getLogger(V_Style.class.getName()).log(Level.SEVERE, null, ex);
                 }
             }

            }
    }//GEN-LAST:event_bt_EnregistrerActionPerformed

    private void bt_ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_ModifierActionPerformed
        // TODO add your handling code here:
        ligneSelect = tb_Style.getSelectedRow();
        if(ligneSelect == -1 ){
            lb_Selection.setForeground(Color.RED);
        }
        else {
            this.affichePanel(false);
            uneCle = (Integer) tb_Style.getValueAt(ligneSelect,0);
            unStyle= lesStyles.get(uneCle);
            ed_Libelle.setText(unStyle.getLibelle());
            mode = 'M';
        }
    }//GEN-LAST:event_bt_ModifierActionPerformed

    private void bt_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_supprimerActionPerformed
        try {
            // TODO add your handling code here:
            
            ligneSelect = tb_Style.getSelectedRow();
        uneCle = (Integer) tb_Style.getValueAt(ligneSelect,0);
            
            if (leControl.estLiee(uneCle)){
                JOptionPane.showMessageDialog(this, "La suppression du Style n'est pas possible car il existe au moins un Album lié à ce Style", "Suppression impossible", 0);
            }
            else {
                ligneSelect = tb_Style.getSelectedRow();
                uneCle = (Integer) tb_Style.getValueAt(ligneSelect,0);
                int dialogResult = JOptionPane.showConfirmDialog (this, "Etes-vous sûre de vouloir supprimer le Style "+ed_Libelle.getText()+" ?","Warning",JOptionPane.YES_NO_OPTION);
                if(dialogResult == JOptionPane.YES_OPTION){
                    try {
                        leControl.sup_Style(uneCle);
                    } catch (SQLException ex) {
                        Logger.getLogger(V_Style.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(V_Style.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bt_supprimerActionPerformed

